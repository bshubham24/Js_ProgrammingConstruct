let day = Math.floor(Math.random()*10)%7;
day++;

if(day==1){
    console.log(day+": Monday");
}
else if(day==2){
    console.log(day+": Tuesday");
}
else if(day==3){
    console.log(day+": Wednesday");
}
else if(day==4){
    console.log(day+": Thursday");
}
else if(day==5){
    console.log(day+": Friday");
}
else if(day==6){
    console.log(day+": Saturday");
}
else if(day==7){
    console.log(day+": Sunday");
}